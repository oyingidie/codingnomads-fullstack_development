# 0. Write a code snippet that gives a name to a `sheep`
#    and uses a boolean value to define whether it has `wool` or not.
# +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

# assign name to sheep
sheep_name = "Fluffy"
# define physical attribute of the animal
has_wool = True

if has_wool:
    print(sheep_name, "has wool.")
else:
    print(sheep_name, "has no wool.")
