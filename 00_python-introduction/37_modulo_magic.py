# 0. Use the modulo operator to confirm which of the values
#    shown below are divisible by seven.
#       - num_1 = 42
#       - num_2 = 137
#       - num_3 = 455
#       - num_4 = 1997
# ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

num_1 = 42
num_2 = 137
num_3 = 455
num_4 = 1997

if (num_1 % 7) == 0:
    print(num_1, "is divisible by seven.")
else:
    print(num_1, "is not divisible by seven.")

if (num_2 % 7) == 0:
    print(num_2, "is divisible by seven.")
else:
    print(num_2, "is not divisible by seven.")

if (num_3 % 7) == 0:
    print(num_3, "is divisible by seven.")
else:
    print(num_3, "is not divisible by seven.")

if (num_4 % 7) == 0:
    print(num_4, "is divisible by seven.")
else:
    print(num_4, "is not divisible by seven.")
