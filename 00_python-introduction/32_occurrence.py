# 0. Find the index of the first occurrence of the letter `n` in the string.
#       - composer = "Ludvig van Beethoven"
# ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

composer = "Ludvig van Beethoven"

# using the find() method
first_occurrence = composer.find("n")
print(first_occurrence)

# using the index() method
first_index = composer.index("n")
print(first_index)
